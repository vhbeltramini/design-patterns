    private void adicionarAcoes() {
        btnMarcarConsulta.addActionListener(e -> controller.marcarConsulta(getMedico(), getPaciente(), getData(), getHora()));

    }

    public void popularPacientes(List<Paciente> pacientes){
        for (Paciente p: pacientes)
            cbPaciente.addItem(p);
    }
    
    public void popularMedicos(List<Medico> medicos){
        for (Medico m: medicos)
            cbMedico.addItem(m);
    }
    
    public Medico getMedico(){
        Medico medico = (Medico) cbMedico.getSelectedItem();
        return medico;
    }
    
    public Paciente getPaciente(){
        Paciente paciente = (Paciente) cbPaciente.getSelectedItem();
        return paciente;
    }
    
    public String getData(){
        return edData.getText();
    }
    
    public String getHora(){
        return edHora.getText();
    }
    
    public void adicionarAcaoBtnMarcarConsulta(ActionListener acao){
        btnMarcarConsulta.addActionListener(acao);
    }

    @Override
    public void exibirMensagem(String msg){
        JOptionPane.showMessageDialog(null, msg);
    }

    @Override
    public void atualizaTela() {
        popularPacientes(controller.getPacientes());
        popularMedicos(controller.getMedicos());
        limparTela();
    }

    public void limparTela(){
        edData.setText("");
        edHora.setText("");
        cbMedico.setSelectedIndex(0);
        cbPaciente.setSelectedIndex(0);
    }
    
    public void exibirTela(){
        setVisible(true);
    }
    

